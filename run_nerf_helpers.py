def invert(mat):
    rot = mat[...,:3,:3]
    trans = mat[...,:3,3,None]
    rot_t = rot.permute(0,2,1)
    # trans_t = -1 * tf.transpose(rot, [0,2,1]) @ trans
    trans_t = -1 * rot.permute(0,2,1) @ trans
    return torch.cat([rot_t, trans_t], -1)

def make_indices(pts, attention_poses, intrinsic, H, W):
    # hom_points = torch.cat([pts, tf.broadcast_to([1.0], pts.shape[:-1] + (1,))], -1)
    a = torch.tensor([1.0])
    hom_points = torch.cat([pts, a.repeat(pts.shape[:-1] + (1,))], -1)
    extrinsic = invert(attention_poses)[:, :3]
    focal = intrinsic[0, 0]

    pt_camera = hom_points[None, ...].repeat(extrinsic.shape[0], hom_points.shape[0], hom_points.shape[1])\
                @ extrinsic.permute(0,2,1)

    pt_camera = focal / pt_camera[:, :, 2][..., None] * pt_camera

    final = 1.0 / focal * (pt_camera @ tf.transpose(intrinsic))
    final = tf.reverse(final, axis=[-1])[..., 1:]
    final = (tf.constant([0., W]) - final) * tf.constant([-1., 1.])
    final = tf.round(final)
    final = tf.math.maximum(tf.math.minimum(final, [H-1.,W-1.]),0)
    final = tf.cast(final, dtype=torch.int32)

    return final
